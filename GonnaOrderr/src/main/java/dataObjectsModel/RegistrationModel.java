package dataObjectsModel;


public class RegistrationModel {

    private String emailTextBox;
    private String passwordTextBox;
    private String confirmPasswordTextBox;
    private String firstNameTextBox;
    private String lastNameTextBox;
    private String countryNameDropdown;
    private String phoneNumberTextBox;

    public String getEmailTextBox() {
        return emailTextBox;
    }

    public void setEmailTextBox(String emailTextBox) {
        this.emailTextBox = emailTextBox;
    }

    public String getPasswordTextBox() {
        return passwordTextBox;
    }

    public void setPasswordTextBox(String passwordTextBox) {
        this.passwordTextBox = passwordTextBox;
    }

    public String getConfirmPasswordTextBox() {
        return confirmPasswordTextBox;
    }

    public void setConfirmPasswordTextBox(String confirmPasswordTextBox) {
        this.confirmPasswordTextBox = confirmPasswordTextBox;
    }

    public String getFirstNameTextBox() {
        return firstNameTextBox;
    }

    public void setFirstNameTextBox(String firstNameTextBox) {
        this.firstNameTextBox = firstNameTextBox;
    }

    public String getLastNameTextBox() {
        return lastNameTextBox;
    }

    public void setLastNameTextBox(String lastNameTextBox) {
        this.lastNameTextBox = lastNameTextBox;
    }

    public String getCountryNameDropdown() {
        return countryNameDropdown;
    }

    public void setCountryNameDropdown(String countryNameDropdown) {
        this.countryNameDropdown = countryNameDropdown;
    }

    public String getPhoneNumberTextBox() {
        return phoneNumberTextBox;
    }

    public void setPhoneNumberTextBox(String phoneNumberTextBox) {
        this.phoneNumberTextBox = phoneNumberTextBox;
    }
}




package dataobjects;

public class TeamDashboard
{
    private String teamMemberName;
    private String teamMemberEmailId;
    private String teamMemberRole;

    public String getTeamMemberName() {
        return teamMemberName;
    }

    public void setTeamMemberName(String teamMemberName) {
        this.teamMemberName = teamMemberName;
    }

    public String getTeamMemberEmailId() {
        return teamMemberEmailId;
    }

    public void setTeamMemberEmailId(String teamMemberEmailId) {
        this.teamMemberEmailId = teamMemberEmailId;
    }

    public String getTeamMemberRole() {
        return teamMemberRole;
    }

    public void setTeamMemberRole(String teamMemberRole) {
        this.teamMemberRole = teamMemberRole;
    }
}
